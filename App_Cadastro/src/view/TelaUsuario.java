    public void preencherTabela(String Sql){
        ArrayList dados = new ArrayList();
        String[] colunas = new String[]{"Id","Nome","Login"};
        con.conectar();
        con.executeSql(Sql);
        try {
            con.rs.first();
            do {                
                 dados.add(new Object[]{con.rs.getString("idusuario"),con.rs.getString("Nome"),con.rs.getString("Login")});
            } while (con.rs.next());
           
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null,"Registro não encontrado");
        }
   TabeloModeloUsuario modelo = new TabeloModeloUsuario(dados, colunas);
   tableusuario.setModel(modelo);
   
   tableusuario.getColumnModel().getColumn(0).setMaxWidth(50);
   tableusuario.getColumnModel().getColumn(0).setResizable(false);// Usuario não poderá o tamanho da coluna
   
   tableusuario.getColumnModel().getColumn(1).setMaxWidth(300);
   tableusuario.getColumnModel().getColumn(1).setResizable(false);
   
   tableusuario.getColumnModel().getColumn(2).setMaxWidth(130);
   tableusuario.getColumnModel().getColumn(2).setResizable(false);
    
   tableusuario.getTableHeader().setReorderingAllowed(false);//Não podera reodernar o cabeçalho
   tableusuario.setSelectionMode(ListSelectionModel.SINGLE_SELECTION); //Seleciona um registro por vez
   
   con.desconectarBanco();
    
    }
    
    
    
