    private void tableusuarioMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tableusuarioMouseClicked
        // TODO add your handling code here:
        String usuario = ""+tableusuario.getValueAt(tableusuario.getSelectedRow(),1);
        con.conectar();
        con.executeSql("SELECT *FROM tbusuario where nome='"+usuario+"'");
        
        try{
            con.rs.first();
            mod.setIduser(Integer.valueOf(con.rs.getString("idusuario")));
            txtNome.setText(String.valueOf(con.rs.getString("nome")));
            txtLogin.setText(String.valueOf(con.rs.getString("login")));
        }catch (SQLException ex){
            JOptionPane.showMessageDialog(null, "Registro não Encontrado");
        }
        con.desconectarBanco();
        txtNome.setEnabled(false);
        txtLogin.setEnabled(false);
        txtSenha.setEnabled(false);
    }//GEN-LAST:event_tableusuarioMouseClicked

